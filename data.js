// Calcolo di trackPoints per il disegno del percorso (normalizzazione delle coordinate geografiche)
const minLat = Math.min(...rawTrackPoints.map(pt => pt.lat));
const maxLat = Math.max(...rawTrackPoints.map(pt => pt.lat));
const minLon = Math.min(...rawTrackPoints.map(pt => pt.lon));
const maxLon = Math.max(...rawTrackPoints.map(pt => pt.lon));

const trackPoints = rawTrackPoints.map(pt => {
  return {
    x: ((pt.lon - minLon) / (maxLon - minLon)) * 800, // Normalizza longitudine su 800px
    y: 600 - ((pt.lat - minLat) / (maxLat - minLat)) * 600, // Normalizza latitudine su 600px
    alt: pt.alt // Mantieni l'altitudine
  };
});

